export function ShareModal({ open, onOpenChange }: ShareModalProps) {
  const url = typeof window !== "undefined" ? window.location.href : "";
  const [copied, setCopied] = useState(false);

  const handleCopy = async () => {
    await navigator.clipboard.writeText(url);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <Modal
      open={open}
      onOpenChange={onOpenChange}
      animation="slide-up"
      size="2xl"
      showCloseButton={true}
      labelId="share-modal-title"
      className="max-w-140"
    >
      {/* Header */}
      <div className="relative flex items-center justify-center px-6 py-4">
        <h2
          id="share-modal-title"
          className="text-grey-950 font-heading text-2xl leading-[1.35] font-normal"
        >
          Share on:
        </h2>
      </div>

      {/* Content */}
      <div className="flex flex-col items-center gap-6 px-6 pb-6">
        {/* Social icons */}
        <div className="flex gap-4">
          {socials.map(({ name, href, icon }) => (
            <a
              key={name}
              href={href(url)}
              target="_blank"
              rel="noopener noreferrer"
              aria-label={`Share on ${name}`}
              className="border-grey-100 hover:border-grey-200 flex items-center justify-center rounded-full border-[0.5px] p-2 transition-colors"
            >
              {icon}
            </a>
          ))}
        </div>

        {/* Copy link */}
        <div className="flex w-full max-w-83 flex-col items-center gap-2">
          <p className="text-grey-950 text-body-sm font-normal">
            Share this link with your friends
          </p>
          <div className="flex w-full gap-2">
            <div className="bg-grey-100 border-grey-200 flex flex-1 items-center overflow-hidden rounded-[1px] border px-4 py-3.5">
              <span className="text-grey-500 text-body-sm truncate">{url}</span>
            </div>
            <Button
              type="button"
              variant="outline"
              fillOnHover
              onClick={handleCopy}
              className="border-grey-800 text-grey-900 text-body-sm h-auto rounded-[1px] px-6 py-3.5 font-normal"
            >
              {copied ? "Copied!" : "Copy"}
            </Button>
          </div>
        </div>
      </div>
    </Modal>
  );
}
